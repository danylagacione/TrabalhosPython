#--- Variáveis

#--- Variável numerica do tipo inteiro
idade = 18
#--- Variável numérica de tipo ponto flutuante
salario = 490.00
#--- Variável texto
nome = 'danieli'
#--- Variávies do tipo booleana
verdadeiro = True
false = False

#--- Impressaão de texto usando multiplicação
print('='*50, '\n'*2)

#--- Impressão de multiplos textos e variávis usando virgula
print('idade', idade, 'salario' ,salario, 'nome' ,nome, 'verdadeiro', verdadeiro, 'false', false)
#--- Impressão de multiplos textos e variávies usando a função format
print ('idade:{} salario:{} nome:{} verdadeiro:{} falso:{}'.format(idade, salario, nome, verdadeiro, false) )
#--- Impressão de multiplos textos e variávies usando interpolação de strings
print(f'idade:{idade} salario:{salario} nome:{nome} verdadeiro:{verdadeiro} falso:{false}') 


#--- Impressaão de texto usando multiplicação
print ('\n'*2, '='*50)
